package calculator;

public class Multiply {
    public static int multiply_two_number(int num1, int num2) {
        return num1 * num2;
    }

    public static float multiply_two_number(float num1, float num2) {
        return num1 * num2;
    }

    public static double multiply_two_number(double num1, double num2) {
        return num1 * num2;
    }
}
