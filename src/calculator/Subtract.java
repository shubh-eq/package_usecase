package calculator;

public class Subtract {
    public static int sub_two_number(int num1, int num2) {
        return num1 - num2;
    }

    public static float sub_two_number(float num1, float num2) {
        return num1 - num2;
    }

    public static double sub_two_number(double num1, double num2) {
        return num1 - num2;
    }
}
