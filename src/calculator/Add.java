package calculator;

public class Add {

    public static int add_two_number(int num1, int num2) {
        return num1 + num2;
    }

    public static float add_two_number(float num1, float num2) {
        return num1 + num2;
    }

    public static double add_two_number(double num1, double num2) {
        return num1 + num2;
    }

}
