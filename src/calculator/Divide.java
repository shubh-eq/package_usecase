package calculator;

public class Divide {
    public static int divide_two_number(int num1, int num2) {
        return num1 / num2;
    }

    public static float divide_two_number(float num1, float num2) {
        return num1 / num2;
    }

    public static double divide_two_number(double num1, double num2) {
        return num1 / num2;
    }
}
